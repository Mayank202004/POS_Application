    public void UpdateComboBox(){
        // This is to get categories for combobox rom categories table in database
        try{
        Statement s = db.mycon().createStatement();
        ResultSet res = s.executeQuery("select * from categories");
        while(res.next()){
        String name=res.getString("Category");
        jComboBox1.addItem(name);
        }
        }
        catch(SQLException e){
        System.out.println(e);
        }
    }
