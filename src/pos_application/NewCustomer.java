    private void rSMaterialButtonRectangle1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonRectangle1ActionPerformed
        String name=jTextField1.getText().trim();
        String phone=jTextField2.getText().trim();
        try{
            Statement s = db.mycon().createStatement();
            s.executeUpdate(" insert into customers (Customername,CustomerPhoneNumber) values ('"+name+"','"+phone+"')");
            ResultSet res = s.executeQuery("select * from customers where CustomerPhoneNumber=('"+phone+"')");
            while(res.next()){
            String id=res.getString("Customerid");
            Sales.setCustomerDetails(id,name,phone);} //calling setter method for sales
            this.dispose();
        }
        catch(SQLException e){
            System.out.println(e);}
        
    }//GEN-LAST:event_rSMaterialButtonRectangle1ActionPerformed

