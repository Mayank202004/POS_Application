    private void search_textKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_textKeyReleased
            // Search By Logic 
      String name = search_text.getText();
      try {
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        dt.setRowCount(0);
        Statement s = db.mycon().createStatement();

        String query = "SELECT ProductID,ProductName,SellingPrice FROM products WHERE ProductName LIKE '%" + name + "%'";
        ResultSet rs = s.executeQuery(query);

        while (rs.next()) {
            Vector v = new Vector();
            v.add(rs.getString(1)); //get column 1
            v.add(rs.getString(2)); //get column 2
            v.add(rs.getString(3)); //get clumn 3
            dt.addRow(v);
        }
      } catch (SQLException e) {
          System.out.println(e);
      }
    }//GEN-LAST:event_search_textKeyReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id=product_id_text.getText();
        try{
            Statement s = db.mycon().createStatement();
            ResultSet rs = s.executeQuery("select ProductName,SellingPrice from products where ProductID="+id);
            boolean found = false;
            while(rs.next()){
                found=true;
                product_name_text.setText(rs.getString("ProductName"));}
            if (!found) {
                // No products found
                JOptionPane.showMessageDialog(null, "No products found with id: " +id);
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "No products found with id: " + id);}
        
    }//GEN-LAST:event_jButton1ActionPerformed

