    //function to preview barcode
    public void previewBarcode(String id) {
    try {
        // Load compiled JasperReports template
        String reportPath = getClass().getResource("/Dependency/barcode.jasper").getPath();

        // Create parameters map
        Map<String, Object> parameters = new HashMap<>();
        parameters.put("productid", id);
        // Fill the report with data (No need to specify data source)
        JasperPrint jasperPrint = JasperFillManager.fillReport(reportPath, parameters,mycon());
        // Preview the report
        JasperViewer.viewReport(jasperPrint, false);
    } catch (JRException e) {
        System.out.println(e);
    }}

